class EcommercewdModelSystempages extends EcommercewdModel {
    ////////////////////////////////////////////////////////////////////////////////////////
    // Events                                                                             //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constants                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    const ERROR_MSG_1 = 'MSG_WRONG_REQUEST';
    const ERROR_MSG_2 = 'MSG_ERROR_LOADING_PAGE';


    ////////////////////////////////////////////////////////////////////////////////////////
    // Variables                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constructor & Destructor                                                           //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Public Methods                                                                     //
    ////////////////////////////////////////////////////////////////////////////////////////
    public function get_error() {
      $error = new stdClass();
      
      $model_options = WDFHelper::get_model('options');
      $options = $model_options->get_options();
      $error->code = get_query_var($options->option_endpoint_systempages_errnum);
      // $error->code = WDFInput::get('code', 0, 'int');

      switch ($error->code) {
        case 1:
          $error->header = __('Error', 'wde') . ' ' . $error->code;
          $error->msg = __(self::ERROR_MSG_1, 'wde');
          break;
        case 2:
          $error->header = __('Error', 'wde') . ' ' . $error->code;
          $error->msg = __(self::ERROR_MSG_2, 'wde');
          break;
        default:
          $error->header = __('Error', 'wde');
          $error->msg = '';
      }

      return $error;
    }


    ////////////////////////////////////////////////////////////////////////////////////////
    // Getters & Setters                                                                  //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Private Methods                                                                    //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Listeners                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
}
